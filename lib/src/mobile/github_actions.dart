    buf.writeln('          bundle exec fastlane ios "\$LANE"');
    buf.writeln();
    buf.writeln('      - name: Upload IPA');
    buf.writeln('        if: success()');
    buf.writeln('        uses: actions/upload-artifact@v4');
    buf.writeln('        with:');
    buf.writeln('          name: ios-ipa');
    buf.writeln('          path: $flutterRel/build/ios/ipa/*.ipa');
    buf.writeln('          if-no-files-found: warn');
    buf.writeln();
    return buf.toString();
  }

  static void _triggers(StringBuffer buf, String flutterRel, String workflowFile) {
    buf.writeln('on:');
    buf.writeln('  workflow_dispatch:');
    buf.writeln('  push:');
    buf.writeln('    branches: [main]');
    buf.writeln('    paths:');
    buf.writeln('      - \'$flutterRel/**\'');
    buf.writeln('      - \'.github/workflows/$workflowFile\'');
    buf.writeln();
  }
